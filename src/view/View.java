    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        
        try {
            int Ciudad = cmbCiudades.getSelectedIndex() + 1;
        
            String Fecha = txtFecha.getText();

            String Dia = Fecha.substring(0,2);
            String Mes = Fecha.substring(3,5);

            String Anio = Fecha.substring(6,10);
            

            String Minima = txtMinima.getText();

            String Maxima = txtMaxima.getText();
            
            int intMinima = Integer.parseInt(Minima);
            int intMaxima = Integer.parseInt(Maxima);

            TemperaturaDAO objCiudad = new TemperaturaDAO();
            
            ControladorFecha controlFecha = new ControladorFecha();
            
            Fecha = Anio + Mes + Dia;

            if (controlFecha.fechaCorrecta(txtFecha.getText()) && intMinima < intMaxima){
                if (!objCiudad.existeTemperatura(Ciudad, Fecha)){
                    objCiudad.grabarTemperaturas(Ciudad, Fecha, Minima, Maxima);
                    JOptionPane.showMessageDialog(null, "Datos cargados correctamente", "InfoBox: " + "Alerta", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Registro ya existente", "InfoBox: " + "Alerta", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                if (intMinima > intMaxima) {
                    JOptionPane.showMessageDialog(null, "La temperatura minima no puede ser mayor a la maxima", "InfoBox: " + "Alerta", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Revise la fecha", "InfoBox: " + "Alerta", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            
            System.out.println(objCiudad.existeTemperatura(Ciudad, Fecha));
        } catch(Exception ex) {
                JOptionPane.showMessageDialog(null, "Revise los datos ingresados", "InfoBox: " + "Alerta", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnCargarActionPerformed

